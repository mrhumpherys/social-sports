const router = require('express').Router();
const { Games, User, Comment, Vote } = require('../../models');

router.get('/', (req, res) => {
    //console.log('=============');
    Games.findAll({
        attributes: [
          'id',
          'game_id', 
          'user_id', 
          'created_at',
        ],
        include: [
            {
              model: Comment,
              attributes: ['id', 'comment_text', 'post_id', 'user_id', 'created_at'],
              include: {
                model: User,
                attributes: ['username']
              }
            },
        ]
    })
    .then(dbPostData => res.json(dbPostData))
    .catch(err => {
        console.log(err);
        res.status(500).json(err);
    });
});

router.get('/:id', (req, res) => {
    Games.findOne({
      where: {
        id: req.params.id
      },
      attributes: [
        'id', 
        'game_id', 
        'user_id', 
        'created_at',
      ],
      include: [
        {
          model: Comment,
          attributes: ['id', 'comment_text', 'post_id', 'user_id', 'created_at'],
          include: {
            model: User,
            attributes: ['username']
          }
        },
      ]
    })
      .then(dbPostData => {
        if (!dbPostData) {
          res.status(404).json({ message: 'No Game found with this id' });
          return;
        }
        res.json(dbPostData);
      })
      .catch(err => {
        console.log(err);
        res.status(500).json(err);
      });
});

